# 1. Дан список A размера N и целое число K (1 < K < N). Преобразовать список,
# увеличив каждый его элемент на исходное значение элемента AK.

def main():
    # Инициализация списка A и целого числа K
    try:
        # Ввод размера списка N и значения K от пользователя
        N = int(input("Введите размер списка A (N > 1): "))
        if N <= 1:
            raise ValueError("Размер списка должен быть больше 1.")

        K = int(input("Введите значение K (1 < K < N): "))
        if not (1 < K < N):
            raise ValueError("Значение K должно быть в диапазоне (1 < K < N).")

        # Ввод элементов списка A
        A = []
        print(f"Введите {N} элементов списка A:")
        for i in range(N):
            element = int(input(f"Элемент {i + 1}: "))
            A.append(element)

        # Вывод исходного списка
        print(f"Исходный список A: {A}")

        # Получение значения элемента AK
        ak = A[K - 1]  # Преобразуем K в индекс (индексация с 0)

        # Преобразование списка A
        result = [x + ak for x in A]

        # Вывод результирующего списка
        print(f"Результирующий список: {result}")

    except ValueError as ve:
        print(f"Ошибка ввода: {ve}")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

# Вызов функции main
main()