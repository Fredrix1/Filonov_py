# 2. Дан целочисленный список A размера N. Переписать в новый целочисленный
# список B того же размера вначале все элементы исходного списка с четными
# номерами, а затем — с нечетными: A2, А4, А6, …, A1, A3, А5, … . Условный
# оператор не использовать.

def main():
    # Инициализация списка A и создание пустого списка B
    try:
        # Ввод размера списка N от пользователя
        N = int(input("Введите размер списка A (N > 0): "))
        if N <= 0:
            raise ValueError("Размер списка должен быть больше 0.")

        # Ввод элементов списка A
        A = []
        print(f"Введите {N} целых чисел для списка A:")
        for i in range(N):
            element = int(input(f"Элемент {i + 1}: "))
            A.append(element)

        # Вывод исходного списка
        print(f"Исходный список A: {A}")

        # Формирование нового списка B с элементами с четными и нечетными индексами
        B = [A[i] for i in range(N) if i % 2 == 0] + [A[i] for i in range(N) if i % 2 != 0]

        # Вывод результирующего списка
        print(f"Результирующий список B: {B}")

    except ValueError as ve:
        print(f"Ошибка ввода: {ve}")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

# Вызов функции main
main()
