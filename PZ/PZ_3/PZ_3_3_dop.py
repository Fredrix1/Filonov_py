# Решить следующие задачи на оценку:
#  Ввести 2 числа. Если их произведение отрицательно, умножить его на 8, в противном
# случае увеличить его в 1.5 раза.
#  Вести число. Если оно четное, разделить его на 4, если нечетное - умножить на 5.
#  Ввести двухзначное число. Если сумма цифр числа четная, то увеличить число на 2,
# в противном случае уменьшить на 2.
#  Дано целое число. Если оно является положительным, то прибавить к нему 20, в
# противном случае вычесть из него 5.
#  Дано два числа. Если их сумма кратна 5, то прибавить 1, иначе вычесть 2


# Задача 1: Ввести 2 числа и обработать их произведение

try:
    num1 = float(input("Введите первое число: "))
    num2 = float(input("Введите второе число: "))

    product = num1 * num2  # Вычисление произведения

    if product < 0:
        product *= 8  # Если произведение отрицательное, умножить на 8
    else:
        product *= 1.5  # Иначе увеличить в 1.5 раза
    print(f"Результат задачи 1: {product}")

except ValueError:
    print("Ошибка ввода: пожалуйста, введите числовые значения.")

# Задача 2: Ввод числа и обработка четности

try:
    number = float(input("Введите число для задачи 2: "))

    if number % 2 == 0:
        result = number / 4  # Если четное, разделить на 4
    else:
        result = number * 5  # Если нечетное, умножить на 5
    print(f"Результат задачи 2: {result}")

except ValueError:
    print("Ошибка ввода: пожалуйста, введите числовое значение.")

# Задача 3: Ввод двухзначного числа и обработка суммы его цифр

try:
    two_digit_number = int(input("Введите двухзначное число: "))

    if 10 <= two_digit_number <= 99:
        digit_sum = (two_digit_number // 10) + (two_digit_number % 10)  # Сумма цифр

        if digit_sum % 2 == 0:
            result = two_digit_number + 2  # Увеличить на 2, если сумма четная
        else:
            result = two_digit_number - 2  # Уменьшить на 2, если сумма нечетная
        print(f"Результат задачи 3: {result}")
    else:
        print("Ошибка: число не является двухзначным.")

except ValueError:
    print("Ошибка ввода: пожалуйста, введите целое число.")

# Задача 4: Ввод целого числа и выполнение условий

try:
    integer_number = int(input("Введите целое число для задачи 4: "))

    if integer_number > 0:
        result = integer_number + 20  # Если положительное, прибавить 20
    else:
        result = integer_number - 5  # В противном случае, вычесть 5
    print(f"Результат задачи 4: {result}")

except ValueError:
    print("Ошибка ввода: пожалуйста, введите целое число.")

# Задача 5: Ввод двух чисел и проверка на кратность

try:
    num1 = float(input("Введите первое число для задачи 5: "))
    num2 = float(input("Введите второе число: "))

    if (num1 + num2) % 5 == 0:
        result = (num1 + num2) + 1  # Если сумма кратна 5, прибавить 1
    else:
        result = (num1 + num2) - 2  # В противном случае, вычесть 2
    print(f"Результат задачи 5: {result}")

except ValueError:
    print("Ошибка ввода: пожалуйста, введите числовые значения.")