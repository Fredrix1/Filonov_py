# Дан символ C и строки S, S0. После каждого вхождения символа C в строку S
# вставить строку S0.

def insert_string_after_char(C, S, S0):
    """
    Вставляет строку S0 после каждого вхождения символа C в строку S.

    :param C: символ, после которого вставляется строка S0.
    :param S: строка, в которой ищутся вхождения символа C.
    :param S0: строка, которая вставляется после каждого символа C.
    :return: строка, полученная после вставки S0 после каждого символа C.
    """
    try:
        # Проверяем, что C — одиночный символ
        if not isinstance(C, str) or len(C) != 1:
            raise ValueError("C должен быть одиночным символом.")

        # Проверяем, что S и S0 — строки
        if not isinstance(S, str) or not isinstance(S0, str):
            raise ValueError("S и S0 должны быть строками.")

        # Вставляем строку S0 после каждого символа C
        result = S.replace(C, C + S0)
        return result

    except ValueError as e:
        return f"Ошибка: {e}"


# Ввод данных с помощью input()
C = input("Введите символ (C): ")
S = input("Введите строку (S): ")
S0 = input("Введите строку, которую нужно вставить после каждого символа C (S0): ")

# Выводим результат
print("Результат:", insert_string_after_char(C, S, S0))
