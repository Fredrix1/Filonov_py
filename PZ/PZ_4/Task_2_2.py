# 2.  Ввести 4 числа. Найти и вывести на экран количество четных чисел.

# Задача 2: Ввод 4 чисел. Нахождение количества четных чисел.
try:
    print("\nЗадача 2: Введите 4 числа для проверки на четность:")
    even_count = 0  # Счетчик четных чисел

    for i in range(4):
        number = int(input(f"Введите число {i + 1}: "))
        if number % 2 == 0:
            even_count += 1

    print(f"Количество четных чисел: {even_count}")

except ValueError:
    print("Ошибка: необходимо ввести целочисленное значение.")