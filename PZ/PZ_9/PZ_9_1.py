# Дан словарь на 6 персон, найти и вывести наибольшее и наименьшее
# значение роста (в см.). (Пример, {"Андрей": 178, "Виктор": 150, "Максим": 200, …},
# наибольшее 200, наименьшее 150)

# Словарь с именами и ростом
people_heights = {
    "Андрей": 178,
    "Виктор": 150,
    "Максим": 200,
    "Ольга": 165,
    "Елена": 170,
    "Иван": 185
}

# Нахождение наибольшего и наименьшего значения роста
max_height = max(people_heights.values())
min_height = min(people_heights.values())

# Вывод результатов
print(f"Наибольшее значение роста: {max_height} см")
print(f"Наименьшее значение роста: {min_height} см")